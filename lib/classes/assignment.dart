class Assignment {
  String id;
  String name;
  String className;
  String classID;
  String authorName;
  String dueDate;
  String description;
  String points;
  String status;

  Assignment({
    required this.id,
    required this.name,
    required this.className,
    required this.classID,
    required this.authorName,
    required this.dueDate,
    required this.description,
    required this.points,
    required this.status,
  });
}